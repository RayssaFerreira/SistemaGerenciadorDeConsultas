     public void validaCampos() throws CampoObrigatorioException {
        String nome = txtNome.getText().trim();
        String cpf = txtCpf.getText().trim();
        String telefone = txtTelefone.getText().trim();
        String endereco = txtEndereco.getText().trim();
        String nascimento = txtNascimento.getText().trim();
        String sexo = "";
        if (rdoF.isSelected()) {
            sexo = "F";
        } else if (rdoM.isSelected()) {
            sexo = "M";
        }

        if (nome.isEmpty() || cpf.isEmpty() || telefone.isEmpty()
                || nascimento.isEmpty() || endereco.isEmpty() || sexo.isEmpty()) {
            throw new CampoObrigatorioException("Prencher os campos obrigat√≥rios");
        }
    }
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            //Recupera Campos Tela
            this.validaCampos();
            String nome = txtNome.getText().trim();
            String cpf = txtCpf.getText().trim();
            String telefone = txtTelefone.getText().trim();
            String endereco = txtEndereco.getText().trim();
            String nascimento = txtNascimento.getText().trim();
            String sexo = "";
            if (rdoF.isSelected()) {
                sexo = "F";
            } else if (rdoM.isSelected()) {
                sexo = "M";
            }
            Paciente paciente = new Paciente();
            paciente.setNome(nome);
            paciente.setCpf(cpf);
            paciente.setTelefone(telefone);
            paciente.setEndereco(endereco);
            paciente.setSexo(sexo);
            try {
                paciente.setData_nascimento(ConverteData.StringForDate(nascimento));
                PacienteBO pacienteBO = new PacienteBO();
                pacienteBO.salvar(paciente);
                JOptionPane.showMessageDialog(this, "Paciente Cadastrado Sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                this.limparCamposTela();
            } catch (CampoObrigatorioException e) {
                JOptionPane.showMessageDialog(this, "Preencha Todos os Campos!", "Erro!", JOptionPane.ERROR_MESSAGE);
            } catch (ParseException e) {
                JOptionPane.showMessageDialog(this, "Data de Nascimento Inv√°lida!", "Erro!", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException e) {
                e.printStackTrace();
            }
        } catch (CampoObrigatorioException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    public void limparCamposTela() {
        txtNome.setText("");
        txtCpf.setText("");
        txtNascimento.setText("");
        txtTelefone.setText("");
        txtEndereco.setText("");
    }

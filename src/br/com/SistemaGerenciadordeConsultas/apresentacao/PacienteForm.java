    private void carregarTabela() {
        PacienteBO pacienteBO = new PacienteBO();
        try {
            this.pacientes = pacienteBO.buscarTodos();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        Model model = new Model();
        this.tblPaciente.setModel(model);
    }

    private class Model extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return pacientes.size();
        }

        @Override
        public int getColumnCount() {
            return 6;
        }

        @Override
        public Object getValueAt(int linha, int coluna) {
            Paciente paciente = pacientes.get(linha);
            if (coluna == 0) {
                return paciente.getNome();
            } else if (coluna == 1) {
                return paciente.getCpf();
            } else if (coluna == 2) {
                return paciente.getTelefone();
            } else if (coluna == 3) {
                return paciente.getEndereco();
            } else if (coluna == 4) {
                return ConverteData.DateForString(paciente.getData_nascimento());
            } else if (coluna == 5) {
                return paciente.getSexo();
            }
            return null;
        }

        @Override
        public String getColumnName(int coluna) {
            if (coluna == 0) {
                return "NOME";
            } else if (coluna == 1) {
                return "CPF";
            } else if (coluna == 2) {
                return "TELEFONE";
            } else if (coluna == 3) {
                return "ENDEREÇO";
            } else if (coluna == 4) {
                return "Nascimento";
            } else if (coluna == 5) {
                return "SEXO";
            }
            return null;
        }
    }
